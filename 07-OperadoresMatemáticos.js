
//Operadores Matemáticos/Aritméticos

/*
    + [SOMA]
    - [SUBTRAÇÃO]
    / [DIVISÃO]
    ++ [INCREMENTO]
    -- [DECREMENTO]
    ** [EXPONENCIAÇÃO]
    % [MÓDULO]
*/

let valor1 = 50;
let valor2 = 5;

let soma = valor1 + valor2;
console.log(`A soma é: ${soma}`);

let subtracao = valor1 - valor2;
console.log(`A subtracao é: ${subtracao}`);

let divisao = valor1 / valor2;
console.log(`A divisao é: ${divisao}`);

let incremento = valor1++;
console.log(`O incremento é: ${incremento}`);

let decremento = valor1--;
console.log(`O decremento é: ${decremento}`);

let exponenciacao = valor1**valor2;
console.log(`A exponenciacao é: ${exponenciacao}`);

let modulo = valor1 % valor2;
console.log(`O módulo da divisão é: ${modulo}`);


